# 메모(주석 : comment)

# 저장 : Ctrl + S
# 실행 : F5
print("안녕하세요")

# 정수 : 소수점X
print(58)

# 실수 : 소수점O
print(3.14)

# 문자 : 쌍따옴표 or 홑따옴표
print('hello, python')
print("hello, python")

# 문자와 문자는 콤마(,)로 연결 가능
print("hello", "python")

# 정수(decimal)
print("%d" % 10)

# 실수(float)
# 소수점 이하 2자리까지만 출력해줘~
print("%.2f" % 3.14)

# 문자 한 개(character)
print("%c" % "B")

# 문자열(string)
print("%s" % "최여정")

# 출력할 데이터가 2개 이상일 경우,
# 반드시 소괄호로 묶어준다.
# 국어 성적이 100점이고, 학점은 A이다.
print("국어 성적이 %d점 이고, 학점은 %c이다." % (100, "A"))


