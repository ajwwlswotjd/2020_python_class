# if문의 구조 3가지
##
##(1)
##if 조건식:
##    조건식이 참일 때, 실행할 문장
##
##(2) 양자택일
##if 조건식:
##    조건식이 참일 때, 실행할 문장
##else:
##    조건식이 거짓일 때, 실행할 문장
##
##(3) 다중택일
##if 조건식1:
##    조건식1이 참일 때, 실행할 문장
##elif 조건식2:
##    조건식2가 참일 때, 실행할 문장
##elif 조건식3:
##    조건식3이 참일 때, 실행할 문장
##else:
##    위 조건을 모두 만족하지 않을 때, 실행할 문장


num = 10

if num % 2 == 0:
    print("짝수")
if num % 2 == 1:
    print("홀수")

#------------------
    
if num % 2 == 0:
    print("짝수")
else:
    print("홀수")

