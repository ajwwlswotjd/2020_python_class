# if - elif 구문은
# 참인 조건식을 만나면
# 이하 조건식은 실행하지 않는다.
if 10 == 10:
    print(10)
if 11 == 11:
    print(11)

if 10 == 10:
    print(10)
elif 11 == 11:
    print(11)


score = 80

if score >= 60:
    print("합격")
else:
    print("불합격")

